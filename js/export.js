'use strict';

// ── SESSION STORE ──
// All logs and outputs are accumulated here for export
const SessionStore = {
  orchestrationLogs: [],
  debateTranscripts: [],
  executionLogs: [],
  securityLogs: [],
  memoryNodes: [],
  pipelineSteps: [],
  modelTestResults: [],
  metadata: {
    sessionStart: new Date().toISOString(),
    version: 'AIOS-X v2.0',
    models: [],
  },
};

function recordLog(category, entry) {
  if (SessionStore[category]) SessionStore[category].push({ ...entry, ts: new Date().toISOString() });
}

// ── MAIN EXPORT ACTION ──
function exportAction(type) {
  const scope = document.querySelector('input[name="exportScope"]:checked')?.value || 'current';
  closeModal('exportModal');

  switch (type) {
    case 'print':   exportPrint(); break;
    case 'markdown': exportMarkdown(scope); break;
    case 'json':    exportJSON(scope); break;
    case 'csv':     exportCSV(); break;
    case 'html':    exportHTML(scope); break;
    case 'clipboard': exportClipboard(scope); break;
  }
}

// ── PRINT / PDF ──
function exportPrint() {
  injectPrintHeader();
  window.print();
  showToast('Print dialog opened', 'info');
}

function injectPrintHeader() {
  const art = document.getElementById('printArtifact');
  if (!art) return;
  art.innerHTML = `
    <div class="print-header">AIOS-X — Agentic Intelligence Orchestration System</div>
    <div class="print-meta">
      Generated: ${new Date().toLocaleString()} | Session: ${SessionStore.metadata.sessionStart}<br>
      Models used: ${SessionStore.metadata.models.join(', ') || 'None'}<br>
      March 2026 AI Landscape Integration Report
    </div>
  `;
}

// ── MARKDOWN REPORT ──
function exportMarkdown(scope) {
  const now = new Date();
  const lines = [
    `# AIOS-X Session Report`,
    `**Generated:** ${now.toLocaleString()}`,
    `**Session Start:** ${SessionStore.metadata.sessionStart}`,
    `**Version:** ${SessionStore.metadata.version}`,
    '',
    '---',
    '',
  ];

  if (scope === 'current' || scope === 'all') {
    const activeTab = document.querySelector('.tab-panel.active');
    if (activeTab) {
      const tabId = activeTab.id.replace('tab-', '');
      lines.push(`## ${getTabTitle(tabId)}`);
      lines.push('');

      // Extract log content
      const logBody = activeTab.querySelector('.log-body');
      if (logBody && logBody.children.length > 0) {
        lines.push('### Log Entries');
        lines.push('```');
        Array.from(logBody.children).forEach(entry => {
          lines.push(entry.innerText || entry.textContent);
        });
        lines.push('```');
        lines.push('');
      }

      // Extract visible text cards
      activeTab.querySelectorAll('.card').forEach(card => {
        const title = card.querySelector('.card-title')?.textContent?.trim();
        if (title) {
          lines.push(`### ${title}`);
          lines.push('');
        }
      });
    }
  }

  if (scope === 'session' || scope === 'all') {
    if (SessionStore.orchestrationLogs.length > 0) {
      lines.push('## Orchestration Log');
      SessionStore.orchestrationLogs.forEach(l => lines.push(`- [${l.ts}] ${l.msg}`));
      lines.push('');
    }

    if (SessionStore.debateTranscripts.length > 0) {
      lines.push('## Debate Transcripts');
      SessionStore.debateTranscripts.forEach(d => {
        lines.push(`### ${d.topic || 'Debate'}`);
        (d.messages || []).forEach(m => {
          lines.push(`**${m.model}:** ${m.text}`);
          lines.push('');
        });
      });
    }

    if (SessionStore.securityLogs.length > 0) {
      lines.push('## Security Events');
      SessionStore.securityLogs.forEach(l => lines.push(`- [${l.ts}] [${(l.level||'info').toUpperCase()}] ${l.msg}`));
      lines.push('');
    }
  }

  lines.push('---');
  lines.push('*Generated by AIOS-X — March 2026 AI Landscape Intelligence System*');

  downloadText(lines.join('\n'), `aiosx-report-${datestamp()}.md`);
  showToast('Markdown report exported', 'success');
}

// ── JSON ARTIFACT ──
function exportJSON(scope) {
  const artifact = {
    meta: {
      ...SessionStore.metadata,
      exportedAt: new Date().toISOString(),
      scope,
    },
    orchestration: scope !== 'current' ? SessionStore.orchestrationLogs : [],
    debate: scope !== 'current' ? SessionStore.debateTranscripts : [],
    execution: scope !== 'current' ? SessionStore.executionLogs : [],
    security: scope !== 'current' ? SessionStore.securityLogs : [],
    memory: scope !== 'current' ? SessionStore.memoryNodes : [],
    modelTests: SessionStore.modelTestResults,
  };

  if (scope === 'current') {
    // Add current panel state
    const activeTab = document.querySelector('.tab-panel.active');
    if (activeTab) {
      artifact.currentPanel = {
        id: activeTab.id,
        textContent: activeTab.innerText?.slice(0, 5000),
      };
    }
  }

  downloadJSON(artifact, `aiosx-artifact-${datestamp()}.json`);
  showToast('JSON artifact exported', 'success');
}

// ── CSV DATA ──
function exportCSV() {
  const rows = [
    ['Model', 'Provider', 'MMLU', 'HumanEval', 'MATH', 'Context', 'Cost/1M In', 'Cost/1M Out', 'Tier'],
  ];

  if (typeof MODEL_REGISTRY !== 'undefined') {
    MODEL_REGISTRY.forEach(m => {
      rows.push([
        m.name, m.provider, m.mmlu, m.humanEval, m.math,
        m.context, m.inputCost, m.outputCost, m.tier,
      ]);
    });
  }

  const csv = rows.map(r => r.map(cell => `"${String(cell).replace(/"/g,'""')}"`).join(',')).join('\n');
  downloadText(csv, `aiosx-models-${datestamp()}.csv`);
  showToast('CSV exported', 'success');
}

