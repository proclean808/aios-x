// ── CLIPBOARD ──
function exportClipboard(scope) {
  const activeTab = document.querySelector('.tab-panel.active');
  const text = activeTab?.innerText?.slice(0, 10000) || 'No content';
  navigator.clipboard.writeText(text).then(() => {
    showToast('Copied to clipboard', 'success');
  }).catch(() => {
    showToast('Clipboard access denied', 'error');
  });
}

// ── SPECIFIC EXPORT HELPERS ──
function exportOrchestratorSession() {
  const logBody = document.getElementById('orchestrationLogBody');
  const entries = logBody ? Array.from(logBody.children).map(e => e.textContent).join('\n') : '';
  const taskInput = document.getElementById('taskInput')?.value || '';

  const content = [
    '# AIOS-X Orchestration Session',
    `Date: ${new Date().toLocaleString()}`,
    '',
    '## Task',
    taskInput,
    '',
    '## Orchestration Log',
    entries,
  ].join('\n');

  downloadText(content, `aiosx-orchestration-${datestamp()}.md`);
  showToast('Orchestration session exported', 'success');
}

function exportDebate() {
  const flow = document.getElementById('debateFlow');
  const synthesis = document.getElementById('synthesisContent');
  const topic = document.getElementById('debateTopic')?.value || 'unknown';

  const lines = [`# AIOS-X Debate Transcript`, `Topic: ${topic}`, `Date: ${new Date().toLocaleString()}`, ''];

  if (flow) {
    Array.from(flow.children).forEach(el => {
      const model = el.querySelector('.dm-model')?.textContent || '';
      const text = el.querySelector('.dm-text')?.textContent || '';
      if (model) lines.push(`**${model}:** ${text}`, '');
    });
  }

  if (synthesis?.textContent) {
    lines.push('## Synthesis', synthesis.textContent);
  }

  downloadText(lines.join('\n'), `aiosx-debate-${datestamp()}.md`);
  showToast('Debate transcript exported', 'success');
}

function exportPipeline() {
  const steps = document.getElementById('pipelineSteps');
  const log = document.getElementById('execLogBody');
  const stepsText = steps ? Array.from(steps.children).map((s,i) => {
    const name = s.querySelector('.step-name')?.textContent || '';
    const agent = s.querySelector('.step-agent')?.textContent || '';
    const status = s.querySelector('.step-status')?.textContent || '';
    return `${i+1}. ${name} [${agent}] — ${status}`;
  }).join('\n') : '';

  const content = [`# AIOS-X Pipeline Export`, `Date: ${new Date().toLocaleString()}`, '', '## Steps', stepsText,
    '', '## Execution Log', log ? Array.from(log.children).map(e => e.textContent).join('\n') : ''].join('\n');

  downloadText(content, `aiosx-pipeline-${datestamp()}.md`);
  showToast('Pipeline exported', 'success');
}

function exportMemory() {
  const nodes = typeof getMemoryNodes === 'function' ? getMemoryNodes() : [];
  downloadJSON({ nodes, exportedAt: new Date().toISOString() }, `aiosx-memory-${datestamp()}.brv.json`);
  showToast('Memory exported as .brv.json', 'success');
}

function exportSecReport() {
  const log = document.getElementById('secLogBody');
  const threats = document.getElementById('threatGrid');
  const content = [
    '# AIOS-X Security Report',
    `Date: ${new Date().toLocaleString()}`,
    '',
    '## Threat Summary',
    threats ? threats.innerText : '',
    '',
    '## Security Events',
    log ? Array.from(log.children).map(e => e.textContent).join('\n') : '',
  ].join('\n');
  downloadText(content, `aiosx-security-report-${datestamp()}.md`);
  showToast('Security report exported', 'success');
}

// ── HELPERS ──
function datestamp() {
  return new Date().toISOString().slice(0, 16).replace(/[T:]/g, '-');
}

function getTabTitle(tabId) {
  const titles = {
    orchestrator: '⬡ Orchestrator',
    models: '🤖 Multi-Model Registry',
    debate: '💬 Debate Arena',
    memory: '🧠 Memory Layer',
    execution: '⚡ Execution Pipeline',
    market: '📊 Market Intelligence',
    security: '🛡️ Security & Compliance',
  };
  return titles[tabId] || tabId;
}

